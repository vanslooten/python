# declare a tuple of 6 fruits
fruits = ("apple", "banana", "cherry", "date", "elderberry", "fig")

# the difference between a tuple and a list is that a tuple is immutable,
# meaning it cannot be changed after creation.

# print the number of fruits
print(f"There are {len(fruits)} fruits in the tuple.")

# print the names of the fruits
for fruit in fruits:
    print(fruit)    
